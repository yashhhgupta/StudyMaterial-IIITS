/* 
 * remainderPower2 - Compute x%(2^n), for 0 <= n <= 30
 *   Negative arguments should yield negative remainders
 *   Examples: remainderPower2(15,2) = 3, remainderPower2(-35,3) = -3
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int remainderPower2(int x, int n) {
    int ans = ~0;  
    int ref = (1 << n) + ans;
    int req = x & ref;
    int sm = x >> 31 << n;
    int hm = !req + ans;
    return (sm & hm) | req;
}
/* howManyBits - return the minimum number of bits required to represent x in
 *             two's complement
 *  Examples: howManyBits(12) = 5
 *            howManyBits(298) = 10
 *            howManyBits(-5) = 4
 *            howManyBits(0)  = 1
 *            howManyBits(-1) = 1
 *            howManyBits(0x80000000) = 32
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 90
 *  Rating: 4
 */
int howManyBits(int x) {
    int r = 0;
    int y = x << 16 >> 16;
    int ref = !(y ^ x);
    int n = ref << 4;
    r = r + n;
    x = x << n;
    ref = !((x << 8 >> 8) ^ x);
    n = ref << 3;
    r = r + n;
    x = x << n;
    ref = !((x << 4 >> 4) ^ x);
    n = ref << 2;
    r = r + n;
    x = x << n;
    ref = !((x << 2 >> 2) ^ x);
    n = ref << 1;
    r =r + n;
    x = x<<n;
    ref = !((x << 1 >> 1) ^ x);
    r = r + ref;
    return 33 + ~r;
}
/* 
 * floatAbsVal - Return bit-level equivalent of absolute value of f for
 *   floating point argument f.
 *   Both the argument and result are passed as unsigned int's, but
 *   they are to be interpreted as the bit-level representations of
 *   single-precision floating point values.
 *   When argument is NaN, return argument..
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 10
 *   Rating: 2
 */
unsigned floatAbsVal(unsigned uf) {
  unsigned ref = uf & 0x7FFFFFFF;
  return ref > 0x7F800000 ? uf : ref;
}
/* 
 * floatFloat2Int - Return bit-level equivalent of expression (int) f
 *   for floating point argument f.
 *   Argument is passed as unsigned int, but
 *   it is to be interpreted as the bit-level representation of a
 *   single-precision floating point value.
 *   Anything out of range (including NaN and infinity) should return
 *   0x80000000u.
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 30
 *   Rating: 4
 */
int floatFloat2Int(unsigned uf) {
  int n = uf >> 31;
    int e = (uf >> 23) & 0xFF;
    int fract = uf & 0x7FFFFF;
    int b = 127;
    int len_fract = 23;

    if (e == 0xFF || e > b + 31)
        return 0x80000000;

    if (e >= b) {
        int power2 = e - b;
        int val = 0;
        if (power2 <= len_fract){
            fract = fract >> (len_fract - power2);
        }
        else{
            fract = fract << (power2 - len_fract);
        }
        val = (1 << power2) | fract;
        return n ? -val : val;
     }
     return 0;
}
/* 
 * floatInt2Float - Return bit-level equivalent of expression (float) x
 *   Result is returned as unsigned int, but
 *   it is to be interpreted as the bit-level representation of a
 *   single-precision floating point values.
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 30
 *   Rating: 4
 */
unsigned floatInt2Float(int x) {
  int e = 158;
    int m = 1<<31;
    int sign = x&m;
    int fraction;
    if (x == m)
        return m | (158<<23);
    if (!x)
        return 0;
    if (sign){
        x = ~x + 1;
    }
    while (!(x&m)) {
        x = x<<1;
        e = e -1;
    }
    fraction = (x&(~m)) >> 8;
    if (x&0x80 && ((x&0x7F) > 0 || fraction&1)){
        fraction = fraction + 1;
    }
    return sign + (e<<23) + fraction;
}
